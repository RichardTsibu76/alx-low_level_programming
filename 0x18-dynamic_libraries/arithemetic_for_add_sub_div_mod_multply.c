#include <stdio.h>
/**
*add  - This adds two numbers
*@a: the first num
*@b: the second number
*Return: a + b
*/
int add(int a, int b)
{
	return a + b;

}

/**
*div - this functions divides two num
*@a: num 1
*@b: num 2
*Return: a / b
*/
int div(int a, int b)
{
	return a / b;
}
/**
*mul - this functions multiplies two numbers
*@a: num 1
*@b: num 2
*Return: a * b
*/
int mul(int a, int b)
{
	return a * b;
}

/**
*sub - This functions takes the difference of two numbers
*@a: The first number
*@b. The second number
*Return: This returns the differece of two nums
*/
int sub(int a, int b)
{
	return a - b;
}
/**
*mod - Remainder
*@a: First num
*@b: The second num
*Return: Returns the remainder
*/
int mod(int a, int b)
{
	return a % b;
}
